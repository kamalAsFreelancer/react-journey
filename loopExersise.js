// Print the number 1-20 using for loop

for (let i = 1; i<=20; i++)
{
    console.log(i);
}

// print only even number from 1-50
for (let j=1; j<=50; j++)
{
    if (j % 2 === 0)
        console.log(j);
}

// calculate the sum  of the num from 1-100
let sum = 0;
for (let k = 1; k<=100; k++){
    sum += k;
}

console.log(sum);